package domain.auxiliares;

public class HuellaPorSector {

    float huella;
    int sectorTerritorial;
    String nombreSector;

    public float getHuella() {
        return huella;
    }

    public int getSectorTerritorial() {
        return sectorTerritorial;
    }

    public String getNombreSector() {
        return nombreSector;
    }

    public void setHuella(float huella) {
        this.huella = huella;
    }
    public void setNombreSector(String nombreSector) {
        this.nombreSector = nombreSector;
    }
    public void setSectorTerritorial(int sectorTerritorial) {
        this.sectorTerritorial = sectorTerritorial;
    }



}

